#define DES_GPIO_HIGH(pin)                                     \
    do                                                         \
    {                                                          \
        uint16_t _addr = 0x0200 + (pin) * 0x3;                 \
        uint8_t  _data = 0x10;                                 \
        Ex_MstWriteBuffer(I2C0_ID, (uint8_t *)&_addr, &_data); \
    } while (0)

#define DES_GPIO_LOW(pin)                                      \
    do                                                         \
    {                                                          \
        uint16_t _addr = 0x0200 + (pin) * 0x3;                 \
        uint8_t  _data = 0x00;                                 \
        Ex_MstWriteBuffer(I2C0_ID, (uint8_t *)&_addr, &_data); \
    } while (0)

